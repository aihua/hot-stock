package assgn.stock.exception;


/**
 * @author Jogireddy Kotam
 *
 */
public class HotStockException extends Exception {

    /**
     * 
     */
    private static final long serialVersionUID = 7260751579967794397L;
    
    public HotStockException() {
        super();
    }

    public HotStockException(String message, Throwable cause) {
        super(message, cause);
    }

    public HotStockException(String message) {
        super(message);
    }

    public HotStockException(Throwable cause) {
        super(cause);
    }


}
